void carregar_mapa(int qtd_inimigos) {
    alocar_mapa_dinamico();
    if (inimigos != NULL) free(inimigos);
    inimigos = (Inimigo*)malloc(qtd_inimigos * sizeof(Inimigo));

    // Carrega o mapa baseado na variável de seleção
    for(int r=0; r<ROWS; r++) {
        for(int c=0; c<COLS; c++) {
            mapa_atual[r][c] = mapas_modelos[indice_mapa_selecionado][r][c];
        }
    }
      
    player.pos.x = TILE_SIZE * 1;
    player.pos.y = TILE_SIZE * 1;
    player.velocidade = 3.0;
    player.dir.x = 0;
    player.dir.y = 0;
    player.pontos = 0;
    player.tem_arma = false;
    player.municao = 0; 
    player.ultimo_dir_x = 1; 

    for(int i=0; i < qtd_inimigos; i++){
        inimigos[i].pos.x = TILE_SIZE * 12;
        inimigos[i].pos.y = TILE_SIZE * 9;
        inimigos[i].velocidade = 1.5;
        inimigos[i].ativo = false; 
        inimigos[i].timer_respawn = i * 4.5; 
    }

    for(int i=0; i<MAX_TIROS; i++) tiros[i].ativo = false;

    total_pontos_no_mapa = 0;
    for(int r=0; r<ROWS; r++){
        for(int c=0; c<COLS; c++) {
            // ALTERADO: Contabiliza pontos normais e a nova Cajuína (9)
            if(mapa_atual[r][c] == 3 || mapa_atual[r][c] == 4 || mapa_atual[r][c] == 9) {
                total_pontos_no_mapa++;
            }
        }
    }

    timer_duracao_arma = 0;
    timer_respawn_arma = 0;
    tempo_de_jogo = 0; 
    recorde_ja_salvo = false; 
    spawnar_item_arma();
}